//Crear un programa que pida al usuario ingresar un número, y luego el porcentaje que se desea obtener del mismo. Devolver un mensaje muestre el porcentaje de dicho número.

const numero = prompt('Ingresa un numero')
const porcentaje = prompt('y ahora luego el porcentaje que se desea obtener del mismo y te dare el porcentaje de dicho número.')
const porcentajeDelNumero = ((numero)*(porcentaje)/100)

alert (porcentajeDelNumero)
